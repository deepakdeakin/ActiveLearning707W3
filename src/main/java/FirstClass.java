
public class FirstClass {
	
	 public static int add(int a, int b) {
	        return a + b;
	    }

	    public static String concat(String str1, String str2) {
	        if (str1 == null) {
	            str1 = "null";
	        }
	        if (str2 == null) {
	            str2 = "null";
	        }
	        return str1 + str2;
	    }

}
